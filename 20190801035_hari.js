var angka=new Date().getDay();
switch (angka){
    case 0:
        console.log("Hari Minggu");
        break;
    case 1:
        console.log("Hari Senin");
        break;
    case 2:
        console.log("Hari Selasa");
        break;
    case 3:
        console.log("Hari Rabu");
        break;
    case 4:
        console.log("Hari Kamis");
        break;
    case 5:
        console.log("Hari Jum'at");
        break;
    case 6:
        console.log("Hari Sabtu");
        break;
}